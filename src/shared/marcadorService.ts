    public actualizarDistancias(array: Marcador[], geolocation: Geolocation){
        array.forEach(marcador => {
            let coords: number[] = [];
            geolocation.getCurrentPosition().then((position) => {
                const latit = position.coords.latitude;
                const longit = position.coords.longitude;
                coords = [latit, longit];
                if (coords != []) {
                    const degtorad = 0.01745329;
                    const radtodeg = 57.29577951;
                    const lat1 = coords[0];
                    const lat2 = marcador.latitud;
                    const long1 = coords[1];
                    const long2 = marcador.longitud;
                    const dlong = (long1 - long2);
                    const dvalue = (Math.sin(lat1 * degtorad) * Math.sin(lat2 * degtorad))
                        + (Math.cos(lat1 * degtorad) * Math.cos(lat2 * degtorad)
                            * Math.cos(dlong * degtorad));
                    const dd = Math.acos(dvalue) * radtodeg;
                    const km = ((dd * 111.302)* 100) / 100;
                    marcador.distancia = km * 1000;
                }
            });
        });
    }


